# Arithemetic Operator
# 1) Addition Operator (+)

print(6 + True)     # 7

print((7+6j) - 9 - 3.23 + True - False)     # (-4.23+6j)

# print(2 + '2')      # TypeError

print('a' + 'b')        # 'ab' -> concatenation

print('23' + '23')      # 2323

print('23' + 'hai')     # 23hai

print([1, 'hai', 9+7j] + [9, 'hello', 10])      # [1, 'hai', (9+7j), 9, 'hello', 10]

print((1, 'hai', 9+7j) + (9, 'hello', 10))      # (1, 'hai', (9+7j), 9, 'hello', 10)

# print({1, 2, 3} + {4, 5})   # TypeError

# print([1, 'hai', 9+7j] + (9, 'hello', 10))      # TypeError

# print({'a': 10} + {'a': 30})        # TypeError\

##############################################################################################

# 2) Subtraction Operator (-)

print(9-8j - True - 3)      # (5-8j)

# print('hai' - 'h')          # TypeError

# print([1, 2, 3, 4] - [2])       # TypeError

# print((1, 2, 3) - (2, 3))           # TypeError

print({1, 2, 3, 4, 5} - {3, 5})     # {1, 2, 4}
print({1, 2, 3, 4, 5}.difference({3, 5}))   # {1, 2, 4}

######################################################################################

# 3) Multiplication Operator

print((6+6j)*(6-6j))        # (72+0j)

# print('hai' * 'h')      # TypeError

print('hai' * 2)        # haihai

print([1, 2, 3] * 2)    # [1, 2, 3, 1, 2, 3]

# print((1, 2, 3) * 2.3)  # TypeError

print((1, 'a', 'b', 3) * 3)    # (1, 'a', 'b', 3, 1, 'a', 'b', 3, 1, 'a', 'b', 3)

# print({1, 2, 3} * 2)        # TypeError

# print({'a': 1} * 2)     # TypeError

#########################################################################################

# 4) True Division (/)

print(4/2)      # 2.0

print((2-2j)/(3-3j))    # (0.6666666666666666+0j)

# print('hai' / 'h')      # TypeError

# print(10 / 0)       # ZeroDivisionError

########################################################################################

# 5) Floor Division (//)

print(24.76 / 1)        # 24.76

print(24.76 // 1)       # 24.0

print(9999 / 1000)      # 9.999

print(9999 // 1000)     # 9

print(-24 / 5)      # -4.8

print(24 // 5)      # 4     -> 4 < 4.8 < 5

print(-24 // 5)     # -5    -> -5 < -4.8 < -4

###################################################################################

# 6) Modulus (%)

print(23 % 10)      # 3

print(57 % 5)       # 2

#####################################################################################

# 7) Power Operator (**)

print(6 ** 2)       # 36

print((-1) ** 0.5)  # (6.123233995736766e-17+1j)        # 1j

print((4+3j) ** 2)  # (7+24j)

